<a name="combining" id="combining"></a><br />
<img src="images/subheading/combining.gif" /><br />
<p>
    <ul>
        <iframe width="420" height="315" src="http://www.youtube.com/embed/SA3C9ABwAqM" frameborder="0" allowfullscreen></iframe><br><br>
        <iframe width="420" height="315" src="http://www.youtube.com/embed/Fqfgy3-CEzA" frameborder="0" allowfullscreen></iframe><br>
    </ul>
	<ol>
			<li>
			<p align="left" style="line-height: 200%"><b>Use good cement: </b>I 
			use best test one coat.&nbsp; Probably Elmer's would work.&nbsp; I 
			wouldn't try to use speed glue.</p></li>
			<li>
			<p align="left" style="line-height: 200%"><b>Paint the sponge: </b>
			Rubber cement is contact cement, so you'll have to have glue on both 
			sponge and topsheet.&nbsp; Allow the sponge to dry flat before 
			putting on the topsheet.</p></li>
			<li>
			<p align="left" style="line-height: 200%"><b>For pips, paint the 
			base sheet: </b>I know some long pips like Neubauer don't recommend 
			you paint the base sheet.&nbsp; This is because it tends to curl up 
			and then it's a mess.&nbsp; I personally do paint it, but if it's a 
			thin base sheet I will put a can on each corner to hold it down 
			until it's dried.&nbsp; You've got to be extremely careful not to 
			let the sheet fold over and touch itself on the glued side.&nbsp; If 
			you do its a disaster.&nbsp; It may take several minutes to dry back 
			flat.&nbsp; Be patient.&nbsp; You don't want it to curl on you.</p>
			</li>
			<li>
			<p align="left" style="line-height: 200%"><b>For inverted, apply 
			glue to tips of pips: </b>This is the trickiest part of gluing an 
			inverted rubber.&nbsp; Personally, I save my plastic the sponge or 
			topsheet came in.&nbsp; Then I spread glue evenly across the 
			plastic.&nbsp; Finally, I lay the pips onto the plastic so that the 
			tips get glue on them.&nbsp; Then I peel it up and examine in the 
			light to make sure that they're all covered.&nbsp; If I missed areas 
			I may repeat the process.&nbsp; In fact, I usually do.&nbsp; Then 
			hold the sheet up and let the tips tack dry before you put it on the 
			sponge.</p></li>
			<li>
			<p align="left" style="line-height: 200%"><b>For 
			pips - Putting on the topsheet: </b>I like to fold my pips in 
			the center BACKWARD so that it's easier to work with.&nbsp; Make 
			sure you fold it so that the pips are touching, not the glued base 
			sheet or you'll be sorry.&nbsp; When it's folded it's easier to work 
			with.&nbsp; Then lay the bottom edge down straight with the sponge.&nbsp; 
			I use a spray can as a roller to roll it down without getting 
			bubbles.&nbsp; If you do get bubbles, it may be easier to just pop 
			them with a needle than to try to peel it up and repeat the whole 
			process.</p></li>
			<li>
			<p align="left" style="line-height: 200%"><b>For inverted - Putting 
			on the topsheet: </b>You can pretty much just lay the topsheet down 
			squarely onto the sponge.&nbsp; If you want to tension the topsheet, 
			you can roll it on with a can or whatever you use, kind of 
			stretching it as you go.&nbsp; Be careful, it's easy to 
			over-tension.&nbsp; You don't have to do much. Just rolling it on at 
			all will create some tension.</p></li>
		</ol>
</p>

