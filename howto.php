        <ol>
            <li>
                <p style="line-height: 200%">
                    <b>Use good cement:</b> I use Best Test One Coat. Probably Elmer's would work. I wouldn't try to use speed glue.
                </p>
            </li>
            <li>
                <p style="line-height: 200%">
                    <b>Paint the sponge:</b> Rubber cement is contact cement, so you'll have to have glue on both sponge and topsheet. Allow the sponge to dry flat before putting on the topsheet.
                </p>
            </li>
            <li>
                <p style="line-height: 200%">
                    <b>For pips, paint the base sheet:</b> I know some long pips like Neubauer don't recommend you paint the base sheet. This is because it tends to curl up and then it's a mess. I personally do paint it, but if it's a thin base sheet I will put a can on each corner to hold it down until it's dried. You've got to be extremely careful not to let the sheet fold over and touch itself on the glued side. If you do its a disaster. It may take several minutes to dry back flat. Be patient. You don't want it to curl on you.
                </p>
            </li>
            <li>
                <p style="line-height: 200%">
                    <b>For inverted, apply glue to tips of pips:</b> This is the trickiest part of gluing an inverted rubber. Personally, I save my plastic the sponge or topsheet came in. Then I spread glue evenly across the plastic. Finally, I lay the pips onto the plastic so that the tips get glue on them. Then I peel it up and examine in the light to make sure that they're all covered. If I missed areas I may repeat the process. In fact, I usually do. Then hold the sheet up and let the tips tack dry before you put it on the sponge.
                </p>
            </li>
            <li>
                <p style="line-height: 200%">
                    <b>For pips - Putting on the topsheet:</b> I like to fold my pips in the center BACKWARD so that it's easier to work with. Make sure you fold it so that the pips are touching, not the glued base sheet or you'll be sorry. When it's folded it's easier to work with. Then lay the bottom edge down straight with the sponge. I use a spray can as a roller to roll it down without getting bubbles. If you do get bubbles, it may be easier to just pop them with a needle than to try to peel it up and repeat the whole process.
                </p>
            </li>
            <li>
                <p style="line-height: 200%">
                    <b>For inverted - Putting on the topsheet:</b> You can pretty much just lay the topsheet down squarely onto the sponge. If you want to tension the topsheet, you can roll it on with a can or whatever you use, kind of stretching it as you go. Be careful, it's easy to over-tension. You don't have to do much. Just rolling it on at all will create some tension.
                </p>
            </li>
        </ol>
