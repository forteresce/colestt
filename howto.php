        <a name="gluing" id="gluing"></a>
        <img src="images/subheading/gluingcutting.gif"/>
        <p>
            Anybody who plans on moving up from a pre-made racket needs to know how to glue on a new sheet of rubbers and cut it neatly to fit their blade. So here's some instructions on how I do it. Personally I use the Zeropong scissors, but I know others have more success with a scalpel or exacto knife. Here's the steps I use:
        </p>
        <ol>
            <li>
                <p style="line-height: 200%">
                    <b>Prime the rubber:</b> I'm a firm believer that rubber plays better with a few layers of glue in the sponge. This is more critical with the firmer Chinese sponges than with softer ones. If a sponge is under 40, I may just give it a couple layers for adhesion. If the sponge is 40-43 I probably will give it 3-5 brushes full. If it's over 43 I may give it as many as 6. Also if it's harder, you may want to wrap it in plastic overnight and let that solvent work on the sponge a little. Even if you don't continue to speed glue, having expanded the sponge at least one time will make it more lively. Any rubber cement is fine for this step. You may want to trace where your blade will be to help you target where to put your glue, if conservation is a goal. If you put a lot of glue on the sponge, you want to let it dry back flat before sticking it on. Otherwise it may shrink back down on the blade. <b>Note: Speed glue is now illegal and regular rubber cement may also test positive for several days. If you plan to go to a sanctioned tournament within a week or so, I would suggest you use a water-based adhesive such as the voc-free glue on the accessories page of this site.</b>
                </p>
                <p>
                    <iframe width="420" height="315" src="http://www.youtube.com/embed/hmB_DQMxtZw" frameborder="0" allowfullscreen></iframe>
                </p>
            </li>    
            <li>
                <p style="line-height: 200%">
                    <b>Apply glue to face of blade:</b> This doesn't have to be very thick. Let it tack dry a couple of minutes before you stick it on.
                </p>
                <p>
                    <iframe width="420" height="315" src="http://www.youtube.com/embed/BzB5_de4rJc" frameborder="0" allowfullscreen></iframe>
                </p>
            </li>
            <li> 
                <p style="line-height: 200%">
                    <b>Lay the rubber on evenly.</b>
                </p>
            </li>
            <li>
                <p style="line-height: 200%">
                    <b>Wet your scissors:</b> Critical step here. I use 409, but any soapy water or really anything slick will do. Even water or spit is better than nothing. It really will help keep the rubber from grabbing the blade.
                </p>
            </li>
            <li>
                <p style="line-height: 200%">
                    <b>Open your scissors wide and keep them that way:</b> Keep the rubber down in the power of the scissors close to the pivot. You'll have to open them especially wide to clear the flange at the start. It would seem like you would want to make smooth long cuts following the blade, but that's what you DON'T want to do. Make many tiny cuts of around 1mm in length. They will actually show up less that way. Be careful of hitting your blade. Take your time and keep the scissors wet.
                </p>
            </li>
            <li>
                <p style="line-height: 200%">
                    <b>Repeat the process:</b> If you're doing both sides that is.
                </p>
                <p>
                    <iframe width="420" height="315" src="http://www.youtube.com/embed/Gn1pqAuZFrM" frameborder="0" allowfullscreen></iframe>
                </p>
            </li>
            <li>
                <p style="line-height: 200%">
                    <b>If using blade instead of scissors:</b> I don't do this much, but the consensus seems to be that if you use a blade, you need to make several passes. Don't try to cut all the way through in one pass.
                </p>
            </li>
        </ol>

